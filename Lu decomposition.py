##############################################################################
#Import library
##############################################################################
import numpy as np
from scipy.linalg import lu , lu_factor, lu_solve
##############################################################################
#Variable
##############################################################################
a = np.array([[30,-10,-10],[-10,110,-100],[-10,-100,120]])
b = np.array([-120,60,0])
##############################################################################
#Decompostion process
##############################################################################
P,L,U = lu(a)
c , piv = lu_factor(a)
x = lu_solve((c,piv),b)

##############################################################################
#Print results and solution
##############################################################################
print('l is :',L,sep='\n')
print('U is :',U,sep='\n')
print('for in')
print('n','value',sep='\t')
for i in range(len(x)):
    print(i+1,x[i],sep='\t')
